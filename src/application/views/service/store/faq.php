<h2>クレジットカード基礎</h2>
<ul class="qa">
  <li>
    <div class="question">
    Q1： 加盟店とは何ですか？
    </div>
    <div class="answer">
    A1：クレジットカード会社と契約している小売店などのことを指します。カード会員は、そのカード会社の加盟店でクレジットカードを利用することができます。米国のクレジットカード業界では、一般的に「マーチャント（merchant）」と呼ぶ場合が多いようです。
    </div>
  </li>

  <li>
    <div class="question">
    Q2：　加盟店手数料とは何ですか？
    </div>
    <div class="answer">
    A2： クレジットカードの加盟店（小売店）でカード会員（消費者）がカードによる買い物をした場合、その加盟店がクレジットカード会社に支払う手数料のことです。なお、カード会社が加盟店に「立替払い」をする際には、売上代金から加盟店手数料を差し引いた金額を支払うことになります。
    </div>
  </li>

  <li>
    <div class="question">
    Q3： クレジットカード端末機とは何ですか？
    </div>
    <div class="answer">
    A3： 電話回線を用いてカードチェックや売上送信を行う機械です。スピーディーな処理により、レジでお客様をお待たせしません。通称CAT（キャット）と言われています。
    </div>
  </li>

  <li>
    <div class="question">
    Q4： クレジットカード端末機ってうちの店に置けるの？
    </div>
    <div class="answer">
    A4： 店舗に電話回線があれば、設置が可能です。（公衆電話や一部の電話回線には、接続できない場合があります。） 回線は、原則、アナログ回線かISDN回線が必要となります。
    </div>
  </li>

  <li>
    <div class="question">
    Q5： うちの店舗は、ブロードバンド対応で、ひかり回線を使用しているけど。
    </div>
    <div class="answer">
    A5：ADSLやひかり回線に対応する端末機もご用意しています。決済回数の多い加盟店様には、プロバイダーやLAN経由で利用できるインターネット通信用の端末機もご用意しています（加盟店様の回線状況やプロバイダーのセキュリティ環境によっては、ご利用できない場合があります）。
    </div>
  </li>

  <li>
    <div class="question">
    Q6： オーソリゼーションとは何ですか？
    </div>
    <div class="answer">
    A6： クレジットカード加盟店は、カード会員が一定金額を超える買い物をしようとする場合、与信可否の承認をカード会社に求めなければなりません。これをオーソリゼーション（信用照会）と言います。原則、クレジットカード端末機にて処理を行うことになります。
    </div>
  </li>

  <li>
    <div class="question">
    Q7： 承認番号とは何ですか？
    </div>
    <div class="answer">
    A7：　クレジットカード加盟店は、一定金額以上の金額の商品をカードで販売する際には、カード発行会社に対し、オーソリゼーションを求めることを義務付けられています。カード会社が承認すれば「承認番号」を加盟店に伝えることになります。
    </div>
  </li>

  <li>
    <div class="question">
    Q8： 分割払いとは、どのような支払方法ですか？
    </div>
    <div class="answer">
    A8： カード利用代金を、会員様のご指定された回数で、均等に分割してカード会社より会員様にご請求させて頂く支払方法です。会員様が分割払いをご利用の場合、カード会社所定の手数料を会員様にご請求させて頂きます。
    </div>
  </li>

  <li>
    <div class="question">  
    Q9： リボルビング払いとは、どのような支払方法ですか？
    </div>
    <div class="answer">
    A9： カード利用の回数や金額にかかわらず、毎月、会員様がご指定されたお支払元金と手数料を、カード会社より会員様にご請求させて頂く支払方法です。
    </div>
  </li>

  <li>
    <div class="question">
    Q10： 利用者の支払方法によって、加盟店手数料は、異なりますか？
    </div>
    <div class="answer">
    A10： 支払方法によって、加盟店手数料が変わることはありません。 （1回払いでも分割払いでも同料率です。）
    </div>
  </li>

  <li>
    <div class="question">
    Q11： （飲食店の場合）営業許可証の写しの提出は必要ですか？
    </div>
    <div class="answer">
    A11： カード会社での審査時に必要となりますので、ご提出をお願い致します。
    </div>
  </li>

  <li>
    <div class="question">
    Q12： お客様にカードの引き落としがいつになるかと聞かれたら、どのように回答すればよいですか？
    </div>
    <div class="answer">
    A12： 引き落とし日は、それぞれの発行カード会社によって異なりますので、カード裏面の発行カード会社へお客様よりご連絡頂くようご案内ください。
    </div>
  </li>

  <li>
    <div class="question">
    Q13：　ＩＣカードとは何ですか？
    </div>
    <div class="answer">
    A13：　ＩＣカードとは、クレジットカードにICチップを埋め込んだカードです。従来の磁気カードより偽造されにくく、安全性・信頼性の高いクレジットカードです。お客様がICカードを提示された場合には、クレジットカード端末機に付属するピンパッドにより、お客様にご自身の暗証番号をご入力頂き、サインレスでカード取引決済が可能となります。
    </div>
  </li>

</ul>




<h2>早期決済サービスについて</h2>
<ul class="qa">
  <li>
    <div class="question">
    Q14： 全てのクレジットカードが、早期決済サービスの対象となるのですか？
    </div>
    <div class="answer">
    A14： 早期決済サービスの対象となるのは、「VISA」「MasterCard」の売上です。「VISA」「MasterCard」の発行枚数は、クレジットカード全体発行枚数の約7割を占めていると言われています。
    </div>
  </li>

  <li>
    <div class="question">
    Q15： 他のカードの売上について、早期決済できないのですか？
    </div>
    <div class="answer">
    A15： 現在は、VISA、MasterCard以外のカードについて、原則、早期決済サービスの提供は行っておりません。ただし、他のカード会社と加盟店契約を締結していただくと、他のクレジットカードも同一の端末機にてご利用できるようにセットすることは可能です。
    </div>
  </li>

  <li>
    <div class="question">
    Q16： 3営業日後振込とは、どのようなものですか？
    </div>
    <div class="answer">
    A16： こちらをご覧ください。
    </div>
  </li>

  <li>
    <div class="question">
    Q17： なぜ3日後に支払えるのですか？
    </div>
    <div class="answer">
    A17： マーチャント・サポートは、端末から通信される売上情報をNTTデータ等の大手データセンター等から毎日取得しています。しかも売上情報は正確かつ安全に伝送され、お客様の売上代金の送金を素早く確実に行えるのです。
    </div>
  </li>

  <li>
    <div class="question">
    Q18： 加盟店へのカード利用代金の支払いは、どのように行われますか？
    </div>
    <div class="answer">
    A18： こちらをご覧ください。
    </div>
  </li>

  <li>
    <div class="question">
    Q19： 支払方法は何が使えますか？
    </div>
    <div class="answer">
    A19： 「1回払い」・「分割払い（3回以上）」・「リボルビング払い」は早期決済の対象となります。「2回払い」・「ボーナス1回払い」はお取扱できますが、早期決済の対象ではありません。（業種によっては1回払いのみの対応となる場合があります。）
    </div>
  </li>

  <li>
    <div class="question">
    Q20： 送金手数料はかかりますか？
    </div>
    <div class="answer">
    A20： 銀行送金手数料は、原則として加盟店様にてご負担頂きます。ただし、送金口座を当社と提携する銀行の各支店にご指定された場合には、当社にて負担致します。また、週1決済の場合も当社で負担致します。
    </div>
  </li>

  <li>
    <div class="question">
    Q21： 早期決済サービスの提供を受ける際に、上限売上金額などの何か制限はありますか？
    </div>
    <div class="answer">
    A21： カード端末機操作やカードのサイン認証確認、暗証番号の入力など、加盟店様が正しいカードの取扱をする限り、支払額には特に制限はありません。ただし、多重売上と思われる売上、加盟店様ごとに設定させて頂いている１件あたりの上限金額を超える取引については、加盟店様とお客様のトラブル防止の観点から、取引内容をご確認させて頂くまで支払を一時的に保留させて頂くことがございます。
    </div>
  </li>

  <li>
    <div class="question">
    Q22： 個人事業主でも申込はできますか？
    </div>
    <div class="answer">
    A22： はい。個人事業主の方でもお申込頂けます。
    </div>
  </li>

  <li>
    <div class="question">
    Q23： 早期決済サービスを受けるための、条件はありますか？
    </div>
    <div class="answer">
    A23： 1.当社指定のクレジットカード会社と新規の加盟店契約を締結して頂く必要があります。（同時に当社へも早期決済サービスの申込を行って頂きます。） 2.新しく、当社の指定するクレジットカード端末機を設置して頂く必要がございます。
    </div>
  </li>

  <li>
    <div class="question">
    Q24： 入会にあたって、審査料や加盟料は発生しますか？
    </div>
    <div class="answer">
    A24： 審査や加盟に料金は発生しません。（無料）
    </div>
  </li>

  <li>
    <div class="question">
    Q25：審査はどこが行うのですか？
    </div>
    <div class="answer">
    A25： クレジットカード会社及び当社にて行います。
    </div>
  </li>

  <li>
    <div class="question">
    Q26： 使用開始までは、どれくらいかかりますか？
    </div>
    <div class="answer">
    A26： 申込書が当社へ到着してから、1～2ヵ月程でクレジットカード端末機を設置させて頂きます。端末機が設置されたその日から使用が可能です。
    </div>
  </li>

  <li>
    <div class="question">
    Q27： 加盟後、加盟店手数料以外にかかる費用はありますか？
    </div>
    <div class="answer">
    A27： 年会費などの費用は、一切かかりません。　（端末機の費用がかかる場合があります。）
    </div>
  </li>

  <li>
    <div class="question">
    Q28： 複数の店舗がある場合の加盟店契約は、どのようになりますか？
    </div>
    <div class="answer">
    A28： 店舗ごとの申込が必要となります。
    </div>
  </li>

  <li>
    <div class="question">
    Q29： 今、設置しているクレジットカード端末を継続使用できますか？
    </div>
    <div class="answer">
    A29： 継続使用はできません。当社が早期決済に必要なデータを取得するためには、当社指定のクレジットカード端末機を新設する必要があります。現在ご使用中の端末は、原則、設置したカード会社へご返却頂くこととなります。
