<h2>Web上のクレジットカード決済について</h2>

<ul class="ul-faq">
  <li>
  <div class="question">
  Q1：Web上で、クレジットカードを取扱いたいのですが、どうすればいいでしょうか？
  </div>
  <div class="answer">
  A1： Web上でクレジットカードを取扱うためには、カード会社との加盟店契約と、カード取引を行う通信インフラが必要となります。当社は、カード加盟店契約を代行するとともに、通信インフラを提供し、低コストで迅速かつ安全なクレジットカード導入が可能となります。
  </div>
  </li>

  <li>
  <div class="question">
  Q2：個人事業ですが、大丈夫でしょうか？
  </div>
  <div class="answer">
  A2： 個人事業でもお申込は可能です。（別途カード会社による審査は必要です。）
  </div>
  </li>

  <li>
  <div class="question">
  Q3：サイトがまだないのですが、申込は可能でしょうか？
  </div>
  <div class="answer">
  A3： 可能です。ただし、審査には、サイトの内容等の情報が必要となりますので、審査に必要な書類等は、当社に相談ください。
  </div>
  </li>

  <li>
  <div class="question">
  Q4：現在の決済代行会社との契約を継続できますか？
  </div>
  <div class="answer">
  A4： 原則、当社が提供する決済代行のASPサービスを利用して頂く必要がございます。
  </div>
  </li>

  <li>
  <div class="question">
  Q5：決済代行のASPサービスの導入について、もう少し詳しく教えてください。
  </div>
  <div class="answer">
  A5： 当社の指定する決済代行会社のASPサービスを導入するためには、ご利用のショッピングカートとの接続が可能かどうかを調べる必要があります。接続実績があれば、特別のソフトウェアの導入等は必要ありません。
  </div>
  </li>

  <li>
  <div class="question">
  Q6：ショッピングカートとの契約がないのですが、どうすればよいのでしょうか？
  </div>
  <div class="answer">
  A6： お客様のニーズに合わせて、当社からショッピングカート運営会社をご紹介させて頂きます。
  </div>
  </li>

  <li>
  <div class="question">
  Q7：現在のショッピングカートをそのまま継続利用できますか？
  </div>
  <div class="answer">
  A7： 当社の指定する決済代行会社との接続実績があれば、可能です。実績がない場合は、継続ご利用はできず、当社指定のショッピングカートに変更するか、現在ご利用のショッピングカート運営会社様に、当社のASPサービスの導入をお願いする必要がございます。
  </div>
  </li>

  <li>
  <div class="question">
  Q8：3-Dセキュアについて、教えてください。
  </div>
  <div class="answer">
  A8： 3-Dセキュアとは、カード決済の際に、カード番号以外に、本人認証のためのID番号等をカード会員に入力させ、カードの不正利用を未然に防ぐものです。⇒詳しくは<a href="web_merit#merit02">こちら</a>をご覧ください。
  </div>
  </li>

  <li>
  <div class="question">  
  Q9：複数のサイトを経営していますが、契約をひとつにできませんか？
  </div>
  <div class="answer">
  A9： 原則、サイトごとにお申込が必要となります。
  </div>
  </li>

  <li>
  <div class="question">
  Q10：携帯サイトでの決済は可能ですか？</a>
  </div>
  <div class="answer">
  A10： 申し訳ありませんが、携帯サイトでの決済サービスの提供は行っておりません。
  </div>
  </li>

</ul>


<h2>早期決済サービスについて</h2>
<ul class="ul-faq">
  <li>
  <div class="question">
  Q11：全てのクレジットカードが早期決済サービスの対象となるのですか？
  </div>
  <div class="answer">
  A11： 原則、国内のカード会社が発行するVISA、MasterCardが早期決済の対象となります。（海外のVISA、MasterCardも、利用（決済）は可能です。国内のVISA、MasterCardと同様に、当社からお支払を致しますが、早期支払の対象ではありません。）
  </div>
  </li>

  <li>
  <div class="question">
  Q12：VISA、MasterCard以外のカードも取扱いたいのですが、可能ですか？
  </div>
  <div class="answer">
  A12： JCB、アメックス、ダイナースクラブについては、同一の決済システムで取扱うことは可能です。（支払は当社指定の決済代行会社からの支払となり、早期決済とはなりません。）
  </div>
  </li>

  <li>
  <div class="question">
  Q13：早期決済の内容について教えてください。
  </div>
  <div class="answer">
  A13： 最短７日後の週1決済となります。詳しくは<a href="web_merit">こちら</a>をご覧ください。
  </div>
  </li>

  <li>
  <div class="question">
  Q14：早期決済サービスを受けるための、条件はありますか？
  </div>
  <div class="answer">
  A14： カード会社による審査がございます。また、販売は原則物品販売とし、デジタルコンテンツ売買の早期決済はお申込できない場合があります。
  </div>
  </li>

  <li>
  <div class="question">
  Q15：申込の際に費用はかかるのでしょうか？
  </div>
  <div class="answer">
  A15： お申込に際しての費用は特に必要ありません。
  </div>
  </li>

  <li>
  <div class="question">
  Q16：導入に関する費用を教えてください。
  </div>
  <div class="answer">
  A16： カード会社の審査が合格したら、インターネット通販のカード決済用ASPサービスの導入を行います。導入費用については、<a href="web_price">こちら</a>をご覧ください。
  </div>
  </li>

  <li>
  <div class="question">
  Q17：加盟後、手数料以外にかかる費用はありますか？
  </div>
  <div class="answer">
  A17： カード決済用ASPサービスの利用料（月額固定）ならびに、通信料がかかります。詳しくは、<a href="web_price">こちら</a>をご覧ください。
  </div>
  </li>

</ul>

