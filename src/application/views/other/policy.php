    <p>
    個人情報の保護及び適正管理を実現するためのコンプライアンスプログラムを策定し、教育訓練及び監督を通じて、従業者に浸透、遵守を図ります。社会環境、法改定等に対応し、定期的に、当該プログラムの改定を実施します。
    </p>

